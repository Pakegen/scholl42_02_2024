void	ft_sort_int_tab(int *tab, int size)
{
	int	i;
	int	swp;

	i = 0;
	while (tab[i] != '\0')
	{
		while (i < (size - 1))
		{
			if (tab[i] > tab[i + 1])
			{
				swp = tab[i];
				tab[i] = tab[i + 1];
				tab[i + 1] = swp;
				i = 0;
			}
			else
			{
				i++;
			}
		}
	}
}
